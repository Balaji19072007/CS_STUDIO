function toSvgTag(name, attrs = {}) {
  const svg = ICONS[name];
  if (!svg) return '';
  const cls = attrs.class ? ` class="${attrs.class}"` : '';
  return svg.replace('<svg', `<svg${cls}`);
}

export const icons = new Proxy({}, {
  get(_, name) {
    if (!ICONS[name]) return undefined;
    return { toSvg: (attrs) => toSvgTag(name, attrs) };
  },
});

export function replace() {
  if (typeof document === 'undefined') return;
  document.querySelectorAll('[data-feather]').forEach((el) => {
    const name = el.getAttribute('data-feather');
    const svg = ICONS[name];
    if (svg) {
      const wrapper = document.createElement('span');
      wrapper.innerHTML = svg;
      const svgEl = wrapper.firstElementChild;
      if (svgEl) {
        Array.from(el.attributes).forEach((attr) => {
          if (attr.name !== 'data-feather') {
            svgEl.setAttribute(attr.name, attr.value);
          }
        });
        el.replaceWith(svgEl);
      }
    }
  });
}
